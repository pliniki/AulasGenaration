package br.com.genaration.poo;

public class ClasseAnimal {
	//atributos
	private String dorme;
	private String come;
	private String Som;
	
	//metodos
	public String getDorme() {
		return dorme;
	}
	public void setDorme(String dorme) {
		this.dorme = dorme;
	}
	public String getCome() {
		return come;
	}
	public void setCome(String come) {
		this.come = come;
	}
	public String getSom() {
		return Som;
	}
	public void setSom(String som) {
		Som = som;
	}
}
